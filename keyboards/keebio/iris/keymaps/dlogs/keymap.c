bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
    case ARROW:
      if (record->event.pressed) {
        SEND_STRING("=> ");
      }
      break;
    case NE:
      if (record->event.pressed) {
        SEND_STRING("!=");
      }
      break;
    case APP:
      if (record->event.pressed) {
         if (get_mods() && MOD_MASK_SHIFT) {
            SEND_STRING("Application");
         } else {
            SEND_STRING("application");
         }
      }
      break;
    case UPST:
      if (record->event.pressed) {
         if (get_mods() && MOD_MASK_SHIFT) {
            SEND_STRING("Upstart");
         } else {
            SEND_STRING("upstart");
         }
      }
      break;
    case DAO:
      if (record->event.pressed) {
         if (get_mods() && MOD_MASK_SHIFT) {
            SEND_STRING("DepositAccountOpening");
         } else {
            SEND_STRING("deposit_account_opening");
         }
      }
      break;
  }
  return true;
}
